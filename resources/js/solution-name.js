(function () {
    // Включаем строгий режим для повышения безопасности, предотвращения утечек памяти и оптимизации кода
    'use strict';

    // 1. Инициализация элементов DOM
    // Ищем целевой селект по атрибуту name="type_val", либо берем первый попавшийся select на странице
    const typeSelect = document.querySelector('select[name="type_val"]') || document.querySelector('select');

    // Собираем все управляемые элементы формы, у которых присутствует обязательный атрибут name
    const formFields = document.querySelectorAll('[name]');

    // Защита от критического падения скрипта: если селектора управления на странице нет, выходим
    if (!typeSelect) {
        console.error('Поле "Тип" не найдено на странице.');
        return;
    }

    /**
     * Вспомогательная функция для поиска логического родительского контейнера поля.
     * Предотвращает дублирование кода (DRY) и гарантирует, что верстка страницы (сетка, отступы) не сломается при скрытии инпутов.
     */
    const getContainer = (field) => {
        return field.closest('p') || field.closest('label') || field.closest('div') || field;
    };

    /**
     * Основная функция фильтрации полей формы
     * Срабатывает при изменении селекта и при первичной загрузке страницы
     */
    function filterFields() {
        // Конвертируем текущую строку значения селекта в целое число (например, "1" -> 1).
        // Всегда явно указываем radix (10) для исключения багов с восьмеричной системой счисления.
        const selectedValue = parseInt(typeSelect.value, 10);

        // ИСПРАВЛЕНИЕ 1: Если фильтр сброшен (выбрано дефолтное пустое значение, строка "0" или NaN), 
        // мы восстанавливаем видимость именно КОНТЕЙНЕРАМ (обертками), а не самим инпутам.
        if (!selectedValue || selectedValue === 0 || isNaN(selectedValue)) {
            formFields.forEach(field => {
                getContainer(field).style.display = '';
            });
            return;
        }

        // Итерируем по всем найденным элементам с атрибутом name
        formFields.forEach(field => {
            // Исключаем сам управляющий селект из цикла фильтрации, чтобы случайно не скрыть его
            if (field === typeSelect) return;

            // Извлекаем значение атрибута name или берем пустую строку, если атрибут отсутствует
            const fieldName = field.getAttribute('name') || '';

            // Вырезаем из имени поля все нецифровые символы (например, "text-12" -> "12")
            const digits = fieldName.replace(/\D/g, '');

            // ИСПРАВЛЕНИЕ 2: Защита от полей без цифр (например, name="submit" или name="_token").
            // Если цифр в строке нет, подставляем '0', чтобы parseInt не вернул NaN, ломая логику сравнения.
            const fieldNum = parseInt(digits || '0', 10);

            // Получаем ссылку на обертку текущего поля с помощью нашей DRY-функции
            const container = getContainer(field);

            // Алгоритм строгого численного сопоставления (полностью решает баг пересечения индексов вроде "1" и "12")
            if (fieldNum === selectedValue) {
                container.style.display = ''; // Отображаем элемент целевой группы (возвращаем дефолтный css-стиль)
            } else {
                container.style.display = 'none'; // Скрываем все нерелевантные элементы
            }
        });
    }

    // 2. Навешиваем обработчик события 'change' для динамического отслеживания выбора пользователя
    typeSelect.addEventListener('change', filterFields);

    // 3. Вызываем функцию принудительно при старте скрипта для первичной фильтрации формы под текущий селект
    filterFields();

    // Лог в консоль разработчика, подтверждающий успешную и безопасную инициализацию плагина/сниппета
    console.log('Скрипт динамических полей успешно активирован.');
})();
